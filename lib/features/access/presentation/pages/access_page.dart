  }

  Widget _buildSegmentedControl(int currentIndex) {
    return Container(
      padding: const EdgeInsets.all(5),
      decoration: BoxDecoration(
        color: AppColors.primaryWhite,
        borderRadius: BorderRadius.circular(18),
        boxShadow: [
          BoxShadow(
            color: const Color(0xFF6A7BA8).withOpacity(0.10),
            blurRadius: 20,
            offset: const Offset(0, 8),
          ),
        ],
      ),
      child: Row(
        children: [
          Expanded(child: _buildSegmentButton(0, 'Pre-Register', currentIndex)),
          Expanded(
            child: _buildSegmentButton(1, 'Active Passes', currentIndex),
          ),
        ],
      ),
    );
  }

  Widget _buildSegmentButton(int index, String label, int currentIndex) {
    final isSelected = index == currentIndex;
    return GestureDetector(
      onTap: () => ref.read(accessTabIndexProvider.notifier).state = index,
      behavior: HitTestBehavior.opaque,
      child: AnimatedContainer(
        duration: const Duration(milliseconds: 200),
        padding: const EdgeInsets.symmetric(vertical: 12),
        decoration: BoxDecoration(
          gradient: isSelected ? AppColors.brandGradient : null,
          borderRadius: BorderRadius.circular(14),
          boxShadow: isSelected
              ? [
                  BoxShadow(
                    color: AppColors.brand.withOpacity(0.30),
                    blurRadius: 12,
                    offset: const Offset(0, 6),
                  ),
                ]
              : [],
        ),
        child: Center(
          child: Text(
            label,
            style: TextStyle(
              fontSize: 14,
              fontWeight: isSelected ? FontWeight.w700 : FontWeight.w600,
              color: isSelected ? Colors.white : AppColors.textSecondary,
            ),
          ),
        ),
      ),
    );
  }

  Widget _buildEntryTypeSelector() {
    const opts = [
      ('single', 'Single', PhosphorIconsRegular.calendarDot),
      ('multiple', 'Multiple', PhosphorIconsRegular.calendarDots),
      ('long_term', 'Long term', PhosphorIconsRegular.calendarPlus),
    ];
    return Row(
      children: [
        for (final o in opts) ...[
          Expanded(
            child: GestureDetector(
              onTap: () => setState(() => _entryType = o.$1),
              child: AnimatedContainer(
                duration: const Duration(milliseconds: 180),
                padding: const EdgeInsets.symmetric(vertical: 12),
                decoration: BoxDecoration(
                  gradient: _entryType == o.$1 ? AppColors.brandGradient : null,
                  color: _entryType == o.$1 ? null : AppColors.surfaceTint,
                  borderRadius: BorderRadius.circular(14),
                ),
                child: Column(
                  children: [
                    Icon(
                      o.$3,
                      size: 18,
                      color: _entryType == o.$1
                          ? Colors.white
                          : AppColors.textSecondary,
                    ),
                    const SizedBox(height: 4),
                    Text(
                      o.$2,
                      style: TextStyle(
                        fontSize: 11.5,
                        fontWeight: FontWeight.w700,
                        color: _entryType == o.$1
                            ? Colors.white
                            : AppColors.textSecondary,
                      ),
                    ),
                  ],
                ),
              ),
            ),
          ),
          if (o.$1 != 'long_term') const SizedBox(width: 8),
        ],
      ],
    );
  }

  Widget _dateField(
    String label,
    DateTime? value,
    ValueChanged<DateTime> onPick,
  ) {
    final ctrl = TextEditingController(
      text: value == null ? '' : DateFormat('MMM d, yyyy').format(value),
    );
    return GestureDetector(
      onTap: () async {
        final now = DateTime.now();
        final d = await showDatePicker(
          context: context,
          initialDate: value ?? now,
          firstDate: now.subtract(const Duration(days: 1)),
          lastDate: now.add(const Duration(days: 365)),
        );
        if (d != null) onPick(d);
      },
      behavior: HitTestBehavior.opaque,
      child: AbsorbPointer(
        child: GlassTextField(
          controller: ctrl,
          hintText: label,
          prefixIcon: PhosphorIconsRegular.calendar,
        ),
      ),
    );
  }

  Widget _buildMultiDayPicker() {
    return Column(
      crossAxisAlignment: CrossAxisAlignment.start,
      children: [
        OutlinedButton.icon(
          onPressed: () async {
            final now = DateTime.now();
            final d = await showDatePicker(
              context: context,
              initialDate: now,
              firstDate: now.subtract(const Duration(days: 1)),
              lastDate: now.add(const Duration(days: 365)),
            );
            if (d != null && !_visitDays.any((x) => x.day == d.day && x.month == d.month && x.year == d.year)) {
              setState(() => _visitDays.add(d));
            }
          },
          icon: const Icon(PhosphorIconsRegular.plus, size: 16),
          label: const Text('Add a day'),
        ),
        if (_visitDays.isNotEmpty)
          Padding(
            padding: const EdgeInsets.only(top: 8),
            child: Wrap(
              spacing: 8,
              runSpacing: 8,
              children: [
                for (final d in _visitDays)
                  Chip(
                    label: Text(DateFormat('MMM d').format(d)),
                    onDeleted: () => setState(() => _visitDays.remove(d)),
                  ),
              ],
            ),
          ),
      ],
    );
  }

  Widget _buildPreRegisterForm() {
    return SingleChildScrollView(
      padding: const EdgeInsets.fromLTRB(24, 0, 24, 120),
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          PremiumCard(
            child: Column(
              crossAxisAlignment: CrossAxisAlignment.start,
              children: [
                const SectionHeader(
                  title: 'Guest details',
                  subtitle: 'We will generate a QR pass for entry',
                ),
                const SizedBox(height: 18),
                GlassTextField(
                  controller: _nameController,
                  hintText: 'Visitor Name',
                  prefixIcon: PhosphorIconsRegular.user,
                ),
                const SizedBox(height: 16),
                GlassTextField(
                  controller: _typeController,
                  hintText: 'Purpose (e.g. Guest, Delivery)',
                  prefixIcon: PhosphorIconsRegular.tag,
                ),
                const SizedBox(height: 16),
                // HCA point 7: choose the pass type. PHH keeps single-visit.
                ...[
                  _buildEntryTypeSelector(),
                  const SizedBox(height: 16),
                ],
                if (_entryType == 'single')
                  GestureDetector(
                    onTap: _pickDateTime,
                    behavior: HitTestBehavior.opaque,
                    child: AbsorbPointer(
                      child: GlassTextField(
                        controller: _dateController,
                        hintText: 'Date & Time (Optional)',
                        prefixIcon: PhosphorIconsRegular.calendar,
                      ),
                    ),
                  ),
                if (_entryType == 'long_term') ...[
                  _dateField(
                    'Valid From',
                    _validFrom,
                    (d) => setState(() => _validFrom = d),
                  ),
                  const SizedBox(height: 16),
                  _dateField(
                    'Valid Until',
                    _validTo,
                    (d) => setState(() => _validTo = d),
                  ),
                ],
                if (_entryType == 'multiple') ...[
                  _buildMultiDayPicker(),
                  const SizedBox(height: 16),
                  GestureDetector(
                    onTap: _pickTimeWindow,
                    behavior: HitTestBehavior.opaque,
                    child: AbsorbPointer(
                      child: GlassTextField(
                        controller: _timeWindowController,
                        hintText: 'Time window — tap to pick',
                        prefixIcon: PhosphorIconsRegular.clock,
                      ),
                    ),
                  ),
                ],
                const SizedBox(height: 16),
                GlassTextField(
                  controller: _plateController,
                  hintText: 'Vehicle Plate (Optional)',
                  prefixIcon: PhosphorIconsRegular.carProfile,
                ),
              ],
            ),
          ),
          const SizedBox(height: 24),
          _isSubmitting
              ? const Center(
                  child: CircularProgressIndicator(color: AppColors.brand),
                )
              : ActionButton(
                  label: 'Generate Pass',
                  onPressed: _submitForm,
                  backgroundColor: AppColors.brand,
                  icon: PhosphorIconsRegular.qrCode,
                ),
        ],
      ),
    );
  }

  Widget _buildActivePasses() {
    final visitorsAsync = ref.watch(myVisitorsProvider);

    return visitorsAsync.when(
      loading: () => const Center(child: CircularProgressIndicator()),
      error: (error, stack) => AppErrorState(
        message: 'Error: $error',
        onRetry: () => ref.read(myVisitorsProvider.notifier).refresh(),
      ),
      data: (visitors) {
        // Show only active passes (not yet checked-out)
        final activeVisitors = visitors
            .where((v) => v.status != 'checked_out' && v.qrToken != null)
            .toList();

        if (activeVisitors.isEmpty) {
          return AppEmptyState(
            icon: PhosphorIconsRegular.qrCode,
            title: 'No active passes',
            message: 'Pre-register a guest to generate a visitor QR pass.',
            gradient: AppColors.skyGradient,
            actionLabel: 'Pre-register a guest',
            onAction: () => ref.read(accessTabIndexProvider.notifier).state = 0,
          );
        }

        return ListView.separated(
          padding: const EdgeInsets.fromLTRB(24, 0, 24, 120),
          itemCount: activeVisitors.length,
          separatorBuilder: (context, index) => const SizedBox(height: 16),
          itemBuilder: (context, index) {
            final visitor = activeVisitors[index];
            final dateStr = visitor.expectedAt != null
                ? DateFormat(
                    'MMM d, yyyy HH:mm',
                  ).format(DateTime.parse(visitor.expectedAt!).toLocal())
                : 'Walk-in';

            // HCA: describe the pass validity on the card (boss feedback).
            String? passType;
            {
              String day(String iso) {
                try {
                  return DateFormat('MMM d').format(DateTime.parse(iso));
                } catch (_) {
                  return iso;
                }
              }

              switch (visitor.entryType) {
                case 'multiple':
                  final days = (visitor.visitDays ?? []).map(day).join(', ');
                  passType =
                      'Multiple days${days.isEmpty ? '' : ': $days'}'
                      '${visitor.timeWindow == null ? '' : ' • ${visitor.timeWindow}'}';
                  break;
                case 'long_term':
                  final from = visitor.validFrom;
                  final to = visitor.validTo;
                  passType =
                      'Long term'
                      '${from != null ? ': ${day(from)}' : ''}'
                      '${to != null ? ' – ${day(to)}' : ''}';
                  break;
                default:
                  passType = 'Single entry';
              }
            }

            return VisitorPassCard(
              name: visitor.visitorName,
              type: visitor.purpose,
              time: dateStr,
              qrData: visitor.qrToken!,
              status: visitor.status,
              passType: passType,
            );
          },
        );
      },
    );
  }
}
