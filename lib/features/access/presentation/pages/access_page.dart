// State Management
final accessTabIndexProvider = StateProvider<int>((ref) => 0);

final myVisitorsProvider =
    AsyncNotifierProvider<MyVisitorsNotifier, List<Visitor>>(
      () => MyVisitorsNotifier(),
    );

class MyVisitorsNotifier extends AsyncNotifier<List<Visitor>> {
  @override
  Future<List<Visitor>> build() async {
    final profile = await ref.watch(currentProfileProvider.future);
    if (profile == null || profile.houseId == null) return [];

    final repo = ref.read(visitorRepositoryProvider);
    return repo.getVisitorsForHouse(profile.houseId!);
  }

  Future<void> refresh() async {
    state = const AsyncValue.loading();
    state = await AsyncValue.guard(() => build());
  }
}

class AccessPage extends ConsumerStatefulWidget {
  const AccessPage({super.key});

  @override
  ConsumerState<AccessPage> createState() => _AccessPageState();
}

class _AccessPageState extends ConsumerState<AccessPage> {
  final _nameController = TextEditingController();
  final _typeController = TextEditingController();
  final _dateController = TextEditingController();
  final _plateController = TextEditingController();
  DateTime? _selectedDateTime;
  bool _isSubmitting = false;
  // HCA point 7: visitor pass entry type + its validity inputs.
  String _entryType = 'single'; // single | multiple | long_term
  DateTime? _validFrom;
  DateTime? _validTo;
  final List<DateTime> _visitDays = [];
  final _timeWindowController = TextEditingController();

  Future<void> _pickDateTime() async {
    final now = DateTime.now();
    final pickedDate = await showDatePicker(
      context: context,
      initialDate: _selectedDateTime ?? now,
      firstDate: now,
      lastDate: now.add(const Duration(days: 365)),
    );
    if (pickedDate == null || !mounted) return;

    final pickedTime = await showTimePicker(
      context: context,
      initialTime: TimeOfDay.fromDateTime(_selectedDateTime ?? now),
    );
    if (!mounted) return;

    final result = DateTime(
      pickedDate.year,
      pickedDate.month,
      pickedDate.day,
      pickedTime?.hour ?? 0,
      pickedTime?.minute ?? 0,
    );
    setState(() {
      _selectedDateTime = result;
      _dateController.text = DateFormat('MMM d, yyyy HH:mm').format(result);
    });
  }

  /// HCA multiple-entry passes: pick the daily time window from the clock
  /// (start time, then end time) instead of typing it.
  Future<void> _pickTimeWindow() async {
    final start = await showTimePicker(
      context: context,
      initialTime: const TimeOfDay(hour: 9, minute: 0),
      helpText: 'Select start time',
    );
    if (start == null || !mounted) return;

    final end = await showTimePicker(
      context: context,
      initialTime: TimeOfDay(hour: (start.hour + 3) % 24, minute: start.minute),
      helpText: 'Select end time',
    );
    if (end == null || !mounted) return;

    setState(() {
      _timeWindowController.text =
          '${start.format(context)} - ${end.format(context)}';
    });
  }

  @override
  void dispose() {
    _nameController.dispose();
    _typeController.dispose();
    _dateController.dispose();
    _plateController.dispose();
    _timeWindowController.dispose();
    super.dispose();
  }

  void _submitForm() async {
    if (_nameController.text.isEmpty) {
      ScaffoldMessenger.of(context).showSnackBar(
        const SnackBar(content: Text('Visitor Name is required.')),
      );
      return;
    }

    setState(() => _isSubmitting = true);

    try {
      final profile = await ref.read(currentProfileProvider.future);
      if (profile == null ||
          profile.houseId == null ||
          profile.houseId!.isEmpty) {
        throw Exception(
          'You must be assigned to a house to register visitors. Please contact Admin.',
        );
      }

      final qrToken = 'v-${DateTime.now().millisecondsSinceEpoch}';

      String d(DateTime x) =>
          '${x.year.toString().padLeft(4, '0')}-${x.month.toString().padLeft(2, '0')}-${x.day.toString().padLeft(2, '0')}';

      final visitor = Visitor(
        id: '', // Will be generated by Supabase
        visitorName: _nameController.text,
        purpose: _typeController.text.isEmpty ? 'Guest' : _typeController.text,
        vehiclePlate: _plateController.text.isEmpty
            ? null
            : _plateController.text,
        houseId: profile.houseId!,
        qrToken: qrToken,
        status: 'expected',
        expectedAt: (_selectedDateTime ?? DateTime.now()).toIso8601String(),
        createdBy: profile.id,
        registrationType: 'pre-registered',
        // Point 7 validity (HCA).
        entryType: _entryType,
        validFrom: (_entryType == 'long_term' && _validFrom != null)
            ? d(_validFrom!)
            : null,
        validTo: (_entryType == 'long_term' && _validTo != null)
            ? d(_validTo!)
            : null,
        visitDays: (_entryType == 'multiple' && _visitDays.isNotEmpty)
            ? _visitDays.map(d).toList()
            : null,
        timeWindow: (_entryType == 'multiple' &&
                _timeWindowController.text.trim().isNotEmpty)
            ? _timeWindowController.text.trim()
            : null,
      );

      await ref.read(visitorRepositoryProvider).createVisitor(visitor);
      await ref.read(myVisitorsProvider.notifier).refresh();

      if (mounted) {
        // Boss feedback 15/07: once the pass is created, the form starts
        // fresh — no leftover data from the previous visitor.
        setState(() {
          _nameController.clear();
          _typeController.clear();
          _dateController.clear();
          _plateController.clear();
          _timeWindowController.clear();
          _selectedDateTime = null;
          _entryType = 'single';
          _validFrom = null;
          _validTo = null;
          _visitDays.clear();
        });
        ref.read(accessTabIndexProvider.notifier).state =
            1; // Switch to passes tab
      }
    } catch (e) {
      if (mounted) {
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(content: Text(e.toString()), backgroundColor: Colors.red),
        );
      }
    } finally {
      if (mounted) {
        setState(() => _isSubmitting = false);
      }
    }
  }

  @override
  Widget build(BuildContext context) {
    final tabIndex = ref.watch(accessTabIndexProvider);

    return Scaffold(
      backgroundColor: AppColors.backgroundGrey,
      body: GradientBackground(
        child: SafeArea(
          child: Column(
            children: [
              Padding(
                padding: const EdgeInsets.fromLTRB(24, 32, 24, 24),
                child: _buildHeader(context),
              ),
              Padding(
                padding: const EdgeInsets.symmetric(horizontal: 24),
                child: _buildSegmentedControl(tabIndex),
              ),
              const SizedBox(height: 24),
              Expanded(
                child: IndexedStack(
                  index: tabIndex,
                  children: [
                    _buildPreRegisterForm()
                        .animate(target: tabIndex == 0 ? 1 : 0)
                        .fade(duration: 300.ms)
                        .slideY(begin: 0.1, end: 0),
                    _buildActivePasses()
                        .animate(target: tabIndex == 1 ? 1 : 0)
                        .fade(duration: 300.ms)
                        .slideY(begin: 0.1, end: 0),
                  ],
                ),
              ),
            ],
          ),
        ),
      ),
    );
  }

